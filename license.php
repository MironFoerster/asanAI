Datenschutzerklärung / Einwilligungserklärung für das Forschungsprojekt »Image Demonstrator«
1. Zweck und datenerhebende Stelle

Im Rahmen des Forschungsprojekts »ScaDS« soll untersucht werden, wie automatische Bildsegmentierung per Computeralgorithmus anhand von wenigen Beispieldaten funktioniert.

Alleinig für vorgenannten Forschungszweck sollen personenbezogene Daten durch die TU Dresden (für das Projekt »ScaDS«, Chemnitzer Straße 46b 01187 Dresden, E-Mail: service@scads.de) verarbeitet werden (Verarbeitung im Sinne von Erhebung, Speicherung, Veränderung und Nutzung). Hierzu ist eine freiwillige und informierte Einwilligung erforderlich.
2. Datenverarbeitung

Die Einwilligungserklärung umfasst die Verarbeitung folgender personenbezogener Daten:

    angegebene Email-Adresse,
    angegebener Nutzername (muss nicht mit Ihrem Klarnamen übereinstimmen!),
    angegebenes Passwort,
    die hochgeladenen Bilder und Segmentierungshilfen.

3. Anonymität

Die Datenerhebung erfolgt personenbezogen. Das bedeutet, dass auf Grund der erhobenen Daten Rückschlüsse auf Sie gezogen werden können. Wir sichern jedoch zu, dass die Daten unverzüglich anonymisiert werden, sobald es der Forschungszweck erlaubt. Es kann jedoch unter Umständen und im Einzelfall möglich sein, dass keine vollständige Anonymisierung möglich ist.
4. Datenübermittlung

Insofern dies nicht anders gesetzlich bestimmt ist oder Sie im Einzelfall ausdrücklich eingewilligt haben, erfolgt keine Übermittlung von personenbezogenen Daten an Dritter
5. Veröffentlichung

Die Forschungsergebnisse werden in wissenschaftlich üblicher Form veröffentlicht. Wir sichern zu, dass aus den Veröffentlichungen keinerlei Rückschlüsse auf natürliche Personen möglich sind.
6. Freiwilligkeit und Widerruf

Die Teilnahme am Forschungsprojekt ist freiwillig. Eine Nichtteilnahme hat keine Folgen!

Diese Einwilligung kann jederzeit schriftlich und formlos bei der datenerhebenden Stelle und mit Wirkung auf die Zukunft widerrufen werden. In diesem Fall reicht eine formlose Email an service@scads.de bzw. über das Kontaktformular.
7. Speicherdauer

Alle personenbezogenen Daten werden nach Abschluss des Forschungsprojektes datenschutzgerecht gelöscht.
8. Datenschutzbeauftragter und Aufsichtsbehörde für den Datenschutz

Mir ist bekannt, dass ich mich jederzeit an den Datenschutzbeauftragten der TU Dresden sowie an die zuständige Aufsichtsbehörde für den Datenschutz wenden kann.
9. Auskunftsrecht

Mir ist bekannt, dass ich jederzeit Auskunft über die zu meiner Person verarbeiteten Daten sowie die möglichen Empfänger dieser Daten, an die diese übermittelt wurden, verlangen kann und mir eine Antwort mit der Frist von einem Monat nach Eingang des Auskunftsersuchens zusteht.
